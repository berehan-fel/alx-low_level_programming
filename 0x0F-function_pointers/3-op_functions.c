/**
 * op_add - adds two numbers.
 * @a: one of the numbers to add.
 * @b: the other number to add.
 *
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts two numbers.
 * @a: the number to subract from.
 * @b: the number to subtract.
 *
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies two numbers.
 * @a: one of the numbers to multiply.
 * @b: the other number to multiply.
 *
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides two numbers.
 * @a: the dividend.
 * @b: the divisor.
 *
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - performs modlulo operation on two numbers.
 * @a: one of the numbers to perform modulus.
 * @b: the moduli
 *
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
